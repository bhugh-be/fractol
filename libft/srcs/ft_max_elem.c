#include "libft.h"

int				ft_max_elem(int arr[], int size)
{
	int			i;
	int			max;

	i = 0;
	max = arr[0];
	while (i < size)
	{
		if (arr[i] > max)
			max = arr[i];
		i++;
	}
	return (max);
}
